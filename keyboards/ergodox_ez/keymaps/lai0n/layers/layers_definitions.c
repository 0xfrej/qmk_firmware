enum layers {        // Hacker Dvorak keyboard layers:
    DVORAK   = 0,    //   * Dvorak base layer.
    ARROWS   = 1,    //   * Arrows movement keys and edition shortcuts.
    MOUSE    = 2,    //   * Mouse movement keys and edition shortcuts.
    NUMPAD   = 3,    //   * ATM style numpad with symbols and letters that should suffice to input any numeric literal.
    MEDIA_FN = 4,    //   * Media and function keys from F1 to F24 in symmetric fashion.
    CHORD    = 5,    //   * Hot keys layer (uses hyper + F1 .. F24) suitable for global shortcut tasks.
};
